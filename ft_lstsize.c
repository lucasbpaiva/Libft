#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		size;
	t_list	*ptr;

	size = 0;
	ptr = lst;
	while (ptr)
	{
		size++;
		ptr = ptr->next;
	}
	return (size);
}
